class Solution {
public:
    vector<int> countSmaller(vector<int>& nums) {
      // æš´åŠ›æ±‚è§£ O(n^2)
      vector<int> res;
      res.resize(nums.size(),0);

      for(int i = 0; i < nums.size();i++){
        int count = 0;
        for(int j = i+1; j < nums.size();j++){
          if(nums[j] < nums[i]){
            count++;
          }
        }
        res[i] = count;
      }
      return res;

    }
};
