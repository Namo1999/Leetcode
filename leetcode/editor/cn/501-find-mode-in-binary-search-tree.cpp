class Solution {
  private:
    int maxCount;
    int count ;
    TreeNode* pre;
    vector<int> result;
    void searchBST(TreeNode* cur){
      if(cur == nullptr)
        return ;
      searchBST(cur->left); // å·¦
      // å¤„ç†ä¸­é—´èŠ‚ç‚¹
      if(pre == nullptr){
        count=1;
      }else if(pre->val == cur->val){
        count++;
      }else{
        count = 1;
      }
      pre =cur;
      if(count == maxCount){
        result.push_back(cur->val);
      }
      if(count > maxCount){
        maxCount = count;
        result.clear();
        result.push_back(cur->val);
      }

      searchBST(cur->right);
      return ;
    }
  public:
    vector<int> findMode(TreeNode* root) {
      count = 0;
      maxCount = 0;
      TreeNode* pre = nullptr;
      result.clear();

      searchBST(root);
      return result;


    }
};
