int main()
{
    Solution s;
    vector<int> data{7, 1, 5, 3, 6, 4};
    ListNode * numberList = nullptr;
    for(int i = 0; i <= data.size()-1;i++){
      numberList = new ListNode(data[data.size()-1-i],numberList);
    }
    ListNode *ptr = numberList;
    while(ptr != nullptr){
      cout << ptr->val << "\t";
      ptr = ptr->next;
    }
    cout << endl;
    auto res=s.removeNthFromEnd(numberList,1);
    while(res != nullptr){
      cout << res->val << "\t";
      res = res->next;
    }

    return 0;

}